using ImgAnalysis, Test

function generateImg(bitImg::BitMatrix; threshold::Real=0.65)
    img = similar(bitImg, Float64)
    @simd for i in eachindex(img)
        @inbounds img[i] = 0.2 * rand() + ifelse(bitImg[i], threshold, 0.0)
    end
    return img
end

